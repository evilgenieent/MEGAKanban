export function pickOne(set) {
  return set[Math.floor(Math.random() * set.length)]
}

export function randomNumber() {
  let number = Math.round(Math.random() * 90) + 10
  let numbers = [
    ['nr', number],
    ['of', 1900 + number],
    [number * 100]
  ]
  return pickOne(numbers)
}

